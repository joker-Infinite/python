#!/usr/bin/python
# -*- coding: UTF-8 -*-
if __name__ == '__main__':
    a = 3
    b = 2
    c = 5
    # 算数运算符
    print('算数运算符')
    '''
    加法
    '''
    print(a + b)
    print(a + b + c)
    '''
    减法
    '''
    print(a - b)
    print(a - b - c)
    '''
    乘法
    '''
    print(a * b)
    print(a * b * c)
    '''
    除法
    '''
    print(a / b)
    print(a / b / c)
    '''
    取模
    '''
    print(a % b)
    print(a % b % c)
    '''
    幂
    '''
    print(a ** b)
    print(a ** b ** c)
    '''
    取整
    '''
    print(a // b)
    print(a // b // c)

    # 比较运算符
    print('比较运算符')
    '''
    等于
    '''
    print(a == b)
    '''
    不等于
    '''
    print(a != b)
    '''
    大于
    '''
    print(a > b)
    '''
    小于
    '''
    print (a < b)
    '''
    大于等于
    '''
    print (a >= b)
    '''
    小于等于
    '''
    print (a <= b)

    # 赋值运算符
    print ('赋值运算符')
    '''
    简单赋值
    '''
    a = b
    print (a)
    '''
    加法赋值
    '''
    a += b
    print (a)
    '''
    减法赋值
    '''
    a -= b
    print (a)
